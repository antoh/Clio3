		<h3>Using Existing Hymn Databases to Explore Religious Language.</h3>
		<p>Hymns are a widely acknowledged source for the study of religious practice and are often seen, along with sermons, as a window onto changing religious beliefs. At the same time, hymns are also a highly complicated source to work with, given that hymn texts appear in multiple variations across multiple denominations. In addition, hymns are participatory and performative, and so take on different meanings in each different performance.</p>
		<p>This website is an exploratory project for examining the potential uses of existing hymn data for sustained study of the language of hymns. While different sources are needed to explore the performance of hymnody, the textual data allows for the study of changing themes and language over time. In addition, the immensity of possible textual data when looking at variation in hymns across hymnals, denominations, and time makes hymn texts an idea case study for digital tools and methodologies.</p>

  		<h3>Conclusion</h3>
  		<p>To end, I would like to offer a few general conclusions that I have drawn from this study. First, the Hymnary database, while incredibly useful, is problematic for historically focused investigations. In part because of the reliance on volunteers, the hymnals that are transcribed tend to reflect the preferences of those volunteers. As such, the data tends to be more recent and tends to favor more traditional, and specifically Reformed, denominations. In addition, the hymn writers that are favored tend to be the more well-known male authors, a fact that limits the applicability of the database for the study of lesser-known and untraditional female authors.</p>
  		<p>That being said, analysis of the data that does exists points to the usefulness of text-mining and topic-modeling tools for studying patterns in religious language and dissemination of religious texts. With a larger set of texts, these tools can reveal different emphases in religious language between male and female authors within and across different traditions. Such a large, distance level, reading has not occured in older, analog studies that focused, due to contraints of time and resources, on the most popular and widely disseminated texts. By reading across the texts for patterns and trends, we can start to see how gender is being negotiated in the religious language of the hymns and how those constructions are linked to concepts of salvation and human flourishing.</p>
  		<p>Additional data is needed for mapping of hymn texts to be useful in a meaningful way. But I am still hopeful that the tracking of publication data will offer new ways to visualize the spread of religious movements across space and time. As such, mapping remains an open question of investigation.</p>
  		<p>In conclusion, while digital data for studying questions of gender and religious language in the nineteenth century is still incomplete, the methods are promising. This should provide additional motivation for the creation of the digital data that would make a more full scale analysis of these and other questions of language and the spread of ideas possible. These two, data creation and analysis, go hand in hand, and to do the later, the groundwork of relevant digital material must be laid. </p>
